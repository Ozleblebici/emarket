<template>
  <div>
    <TopHeader></TopHeader>
    <b-container>
      <b-row class="m-5" align-h="center">
        <b-col cols="6" class="text-center">
          <b-card
            border-variant="primary"
            header="Payment Succeeded"
            header-bg-variant="primary"
            header-text-variant="white"
            align="center"
          >
            <b-button size="lg" disabled variant="primary">
              <b-icon icon="check" scale="3"></b-icon>
            </b-button>
            <h1 class="mt-4">You're all set!</h1>
            <h6>Thanks for being awesome,</h6>
            <h6>we hope you enjoy your purchase!</h6>
            <b-card-footer class="mt-4"> estorebd.com </b-card-footer>
          </b-card>
        </b-col>
      </b-row>
    </b-container>
    <Footer></Footer>
  </div>
</template>

<script>
import TopHeader from "@/components/TopHeader.vue";
import Footer from "@/components/Footer.vue";
import CheckoutService from "@/services/CheckoutService.js";
import OrderService from "../../services/OrderService";
import OrderItemService from "../../services/OrderItemService";
export default {
  name: "SuccessPayment",
  components: {
    TopHeader,
    Footer
  },
  data() {
    return {
      order: {},
      session: {}
    };
  },
  async mounted() {
    const sessionId = this.$store.state.route.query.id;
    try {
      this.session = (
        await CheckoutService.retrieveCheckoutSession(sessionId)
      ).data;
    } catch (error) {
      console.log(error.response.data.error);
    }
    const lineItems = this.session.line_items.data;
    try {
      this.order = (
        await OrderService.createOrder({
          name: this.session.metadata.customerName,
          phoneNo: this.session.metadata.customerPhoneNo,
          email: this.session.customer_email,
          address: this.session.metadata.shippingAddress,
          status: "paid",
          variant: "dark",
          checkoutSessionId: sessionId,
          productCost: this.session.amount_total / 100,
          currency: this.session.currency.toUpperCase(),
          shippingCost: lineItems[lineItems.length - 1].amount_total / 100
        })
      ).data;
    } catch (error) {
      console.log(error.response.data.error);
    }

    var i;
    for (i in lineItems) {
      try {
        await OrderItemService.createOrderItem({
          quantity: lineItems[i].quantity,
          ProductId: parseInt(lineItems[i].description),
          OrderId: this.order.id
        });
      } catch (error) {
        console.log(error.response.data.error);
      }
    }
  },
  methods: {},
  computed: {}
};
</script>

