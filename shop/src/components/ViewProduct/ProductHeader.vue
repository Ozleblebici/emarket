<template>
  <div>
    <b-navbar size="sm" toggleable="md" type="primary" variant="white">
      <b-navbar-brand href="#" @click="productHome">
        <b-icon variant="primary" icon="house-door-fill" />
        <b-icon class="ml-2" scale=".7" icon="chevron-right" />
      </b-navbar-brand>
      <b-navbar-toggle target="nav-products-collapse">
        <b-button-toolbar>
          <b-button-group>
            <b-button variant="outline-dark" class="px-3 py-2">
              <b-icon scale="1.6" icon="list" />
            </b-button>
          </b-button-group>
        </b-button-toolbar>
      </b-navbar-toggle>
      <b-collapse id="nav-products-collapse" is-nav>
        <b-navbar-nav class="mr-auto">
          <b-nav-item @click="setCategory">
            {{ categoryName }}
            <b-icon class="ml-2" scale=".7" icon="chevron-right" />
          </b-nav-item>
          <b-nav-item @click="setSubCategory">
            {{ subCategoryName }}
            <b-icon class="ml-2" scale=".7" icon="chevron-right" />
          </b-nav-item>
          <b-nav-item @click="setSubSubCategory">
            {{ subSubCategoryName }}
            <b-icon class="ml-2" scale=".7" icon="chevron-right" />
          </b-nav-item>
          <b-nav-item to="#">
            {{ productName }}
          </b-nav-item>
        </b-navbar-nav>
        <b-navbar-nav class="ml-auto">
          <b-button pill variant="warning">
            Edit Item
          </b-button>
        </b-navbar-nav>
      </b-collapse>
    </b-navbar>
    <hr class="m-0" />
  </div>
</template>

<script>
export default {
  name: "ProductHeader",
  data() {
    return {
      productName: this.pName,
      categoryId: this.categId,
      categoryName: "",
      subCategoryId: this.subCategId,
      subCategoryName: "",
      subSubCategoryId: this.subSubCategId,
      subSubCategoryName: ""
    };
  },
  props: {
    pName: String,
    categId: Number,
    subCategId: Number,
    subSubCategId: Number
  },
  components: {},
  computed: {},
  async mounted() {
    if (this.categoryId && this.subCategoryId && this.subSubCategoryId) {
      this.categoryName = await this.$store.dispatch(
        "Category/getCategoryName",
        this.categoryId
      );
      this.subCategoryName = await this.$store.dispatch(
        "Category/getSubCategoryName",
        this.subCategoryId
      );
      this.subSubCategoryName = await this.$store.dispatch(
        "Category/getSubSubCategoryName",
        this.subSubCategoryId
      );
    }
  },
  methods: {
    async productHome() {
      await this.$store.dispatch("Products/resetSearchParameter");
      window.location.replace("/products");
    },
    setCategory() {
      const newRoute = "/products/" + this.categoryName;
      window.location.replace(newRoute);
    },
    setSubCategory() {
      const newRoute =
        "/products/" + this.categoryName + "/" + this.subCategoryName;
      window.location.replace(newRoute);
    },
    setSubSubCategory() {
      const newRoute =
        "/products/" +
        this.categoryName +
        "/" +
        this.subCategoryName +
        "/" +
        this.subSubCategoryName;
      window.location.replace(newRoute);
    }
  }
};
</script>

